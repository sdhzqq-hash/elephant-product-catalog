  let currentLang = localStorage.getItem(STORAGE_KEY) || 'en';
  let translations = I18N_DATA.en;

  function getBrowserLang() {
    var lang = (navigator.language || 'en').split('-')[0];
    return ['en','ru','ko','ja','ar','it'].indexOf(lang) >= 0 ? lang : null;
  }

  function loadLang(lang) {
    if (!I18N_DATA[lang]) { lang = 'en'; }
    translations = I18N_DATA[lang];
    currentLang = lang;
    localStorage.setItem(STORAGE_KEY, lang);
    applyTranslations();
    updateSwitcherUI();
    updateDocumentDirection();
  }

  function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(function(el) {
      var key = el.getAttribute('data-i18n');
      if (translations[key] !== undefined) {
        el.textContent = translations[key];
      }
    });
    if (translations.site_title) {
      document.title = translations.site_title;
    }
  }

  function updateSwitcherUI() {
    document.querySelectorAll('.lang-btn').forEach(function(btn) {
      btn.classList.toggle('active', btn.getAttribute('data-lang') === currentLang);
    });
  }

  function updateDocumentDirection() {
    var dir = translations.dir || 'ltr';
    document.documentElement.setAttribute('dir', dir);
    document.documentElement.setAttribute('lang', currentLang);
  }

  function switchLang(lang) {
    if (lang === currentLang) return;
    loadLang(lang);
  }

  // Init
  function init() {
    if (!localStorage.getItem(STORAGE_KEY)) {
      var browser = getBrowserLang();
      if (browser) currentLang = browser;
    }
    loadLang(currentLang);
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    init();
  }

  window.i18nSwitch = switchLang;
  window.i18nGetLang = function() { return currentLang; };
